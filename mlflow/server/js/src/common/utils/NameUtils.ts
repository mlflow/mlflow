export const generateRandomName = (prefix: string): string => {
  const noun = NOUNS[Math.floor(Math.random() * NOUNS.length)];
  const digits = Math.floor(1000 + Math.random() * 9000);
  return `${prefix}-${noun}-${digits}`;
};
